def even_weighted(lst):
    """
    >>> x = [1, 2, 3, 4, 5, 6]
    >>> even_weighted(x)
    [0, 6, 20]
    """
    return [element * lst.index(element) for element in lst if lst.index(element) % 2 == 0]

